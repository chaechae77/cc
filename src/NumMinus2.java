public class NumMinus2 {

    public int solution(int num1, int num2){
        int answer = 0;
        answer = num1 - num2;
        return answer;

    }


    public static void main(String[] args) {
        NumMinus2 sc = new NumMinus2();
        int num1 = 100;
        int num2 = 2;
        System.out.println(sc.solution( num1 , num2)); //머가 문
    }

}




